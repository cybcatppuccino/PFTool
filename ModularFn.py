def format_to_scientific(value):
    formatted_value = f"{value:.9e}"
    return formatted_value

def makeval(inval):
    re = float(mpmath.re(inval))
    im = float(mpmath.im(inval))
    return "(" + format_to_scientific(re) + ',' + format_to_scientific(im) + ")"

def f3(a,b,c):
    val = (c - a)/(c - b)
    return makeval(val)
def f4(a,b,c,d):
    val = ((d - a)*(b - c))/((d - c)*(b - a))
    return makeval(val)

def ordersubset(lst, k):
    if k == 0:
        return [[]]
    else:
        outlst = []
        for i in range(len(lst)):
            rest = lst[:i] + lst[i+1:]
            outlst += [[lst[i]] + subset for subset in ordersubset(rest, k-1)]
        return outlst
    
def gen3(lst, lbl):
    return [(f3(subset[0], subset[1], subset[2]), lbl) for subset in ordersubset(lst, 3)]
def gen4(lst, lbl):
    return [(f4(subset[0], subset[1], subset[2], subset[3]), lbl) for subset in ordersubset(lst, 4)]
def gen(lst, lbl):
    return gen3(lst, lbl) + gen4(lst, lbl)

